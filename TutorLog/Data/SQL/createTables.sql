BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "LogEntryTopic" (
	"LogEntryID"	INTEGER NOT NULL,
	"TopicID"	INTEGER NOT NULL,
	PRIMARY KEY("TopicID","LogEntryID"),
	FOREIGN KEY("LogEntryID") REFERENCES "LogEntry"("ID"),
	FOREIGN KEY("TopicID") REFERENCES "Topic"("ID")
);
CREATE TABLE IF NOT EXISTS "LogEntry" (
	"ID"	INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
	"CenterID"	INTEGER NOT NULL,
	"CampusID"	INTEGER NOT NULL,
	"StudentID"	TEXT NOT NULL,
	"FName"	TEXT NOT NULL,
	"LName"	TEXT NOT NULL,
	"CourseID"	INTEGER NOT NULL,
	"TutorID"	INTEGER NOT NULL,
	"Timestamp"	TEXT NOT NULL,
	FOREIGN KEY("CenterID") REFERENCES "Center"("ID"),
	FOREIGN KEY("CampusID") REFERENCES "Campus"("ID"),
	FOREIGN KEY("CourseID") REFERENCES "Course"("ID"),
	FOREIGN KEY("TutorID") REFERENCES "Tutor"("ID")
);
CREATE TABLE IF NOT EXISTS "CampusTutor" (
	"CampusID"	INTEGER NOT NULL,
	"TutorID"	INTEGER NOT NULL,
	PRIMARY KEY("CampusID","TutorID"),
	FOREIGN KEY("CampusID") REFERENCES "Campus"("ID"),
	FOREIGN KEY("TutorID") REFERENCES "Tutor"("ID")
);
CREATE TABLE IF NOT EXISTS "CourseTopic" (
	"CourseID"	INTEGER NOT NULL,
	"TopicID"	INTEGER NOT NULL,
	PRIMARY KEY("CourseID","TopicID"),
	FOREIGN KEY("CourseID") REFERENCES "Course"("ID"),
	FOREIGN KEY("TopicID") REFERENCES "Topic"("ID")
);
CREATE TABLE IF NOT EXISTS "Tutor" (
	"ID"	INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
	"FName"	TEXT NOT NULL,
	"LName"	TEXT NOT NULL,
	"IsActive"	INTEGER NOT NULL DEFAULT 1
);
CREATE TABLE IF NOT EXISTS "Topic" (
	"ID"	INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
	"Description"	TEXT NOT NULL UNIQUE
);
CREATE TABLE IF NOT EXISTS "Campus" (
	"ID"	INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
	"Name"	TEXT NOT NULL UNIQUE
);
CREATE TABLE IF NOT EXISTS "Course" (
	"ID"	INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
	"Name"	TEXT NOT NULL UNIQUE
);
CREATE TABLE IF NOT EXISTS "Center" (
	"ID"	INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
	"Name"	TEXT NOT NULL UNIQUE
);
